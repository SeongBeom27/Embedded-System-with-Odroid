// 알파벳, 시작하는 x, y와 넘겨주면 그 곳부터 24 * 24로 문자를 출력해주는 함수
// print 후 커서의 위치도 계속 바꿔준다.
void print(int alpha[][24], int x, int y, int key)
{
    int i, j;
    for (i = x; i < x + 24; i++)
    {
        for (j = y; j < y + 24; j++)
        {
            offset = i * fbvar.xres + j;   /* 위치 */
            pixel = makepixel(0, 0, 0);
            total[i][j] = 0;
            *(pfbdata + offset) = pixel;   
        }
    }

    for (i = x; i < x + 24; i++)
    {
        for (j = y; j < y + 24; j++)
        {
            offset = i * fbvar.xres + j;   /* 위치 */
            if (alpha[i - cur.x][j - cur.y] > 0)
            {
                pixel = makepixel(255, 255, 255);
                total[i][j] = 1;
            }
            else {
                pixel = makepixel(0, 0, 0);
                total[i][j] = 0;
            }
            *(pfbdata + offset) = pixel;   
        }
    }
    return;
}

// x 의 max : 192, 중감 : 32
// y 의 max : 252, 중감 : 28

// 해당 문자를 지우는 함수, 모든 픽셀이 한칸 앞으로 당겨져 와야한다.
// y + 32 에 있는 문자들이 y 위치로 와야한다.
void erase(int x, int y)
{
    int i, j;
    int temp[24][24];


    // total 에 있는 값 (뒤 칸에 있는 값)을 temp에 받아온다.
    for (i = x; i < x + 24; i++)
    {
        for (j = y; j < y + 24; j++)
        {
            // 뒤에 값을 가져오는 것인데
            // 맨 오른쪽에 있던 값은 다음 줄 맨 앞의 값을 가져와야 한다.
            if (y == 252)
            {
                if (x == 192)           // 맨 마지막 문자 처리
                {
                    total[i][j] = 0;
                    temp[i - x][j - y] = 0;
                }
                else {
                    total[i][j] = total[i + 32][j - y];
                    temp[i - x][j - y] = total[i + 32][j - y];
                }
            }
            else {
                total[i][j] = total[i][j + 28];
                temp[i - x][j - y] = total[i][j + 28];
            }
        }
    }
    // temp 에 있는 값 즉 뒤에있던 값을 
    for (i = x; i < x + 24; i++)
    {
        for (j = y; j < y + 24; j++)
        {
            offset = i * fbvar.xres + j;   /* 위치 */
            if (temp[i - x][j - y] > 0)
            {
                pixel = makepixel(255, 255, 255);
                total[i][j] = 1;
            }
            else {
                pixel = makepixel(0, 0, 0);
                total[i][j] = 0;
            }
            *(pfbdata + offset) = pixel;   /* write��를 ��하��지 ��않��아��도 ��된��다. */
        }
    }
    if (x == 192 && y == 252)
    {
        return;
    }
    y += 28;
    if (y == 280)
    {
        x += 32;
        y = 0;
    }
    erase(x, y);
}

// cursor 위치가 blinking 해야한다.
void blinking_cursor(int x, int y)
{
    int i, j;
    if (x == 224) return;
    x += 24;

    
    for (j = y; j < y + 24; j++)
    {
        offset = x * fbvar.xres + j;   /* 위치 */
        pixel = makepixel(255, 255, 255);
        *(pfbdata + offset) = pixel;   
    }

    delay(50);
    
    for (j = y; j < y + 24; j++)
    {
        offset = x * fbvar.xres + j;   /* 위치 */
        pixel = makepixel(0, 0, 0);
        *(pfbdata + offset) = pixel;  
    }
    delay(50);
}

